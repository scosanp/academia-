export const courseModules: CourseModule[] = seeds.map((module) => ({
  ...module,
  lessons: module.lessons.map<Lesson>((lesson) => ({
    id: lesson.id,
    moduleId: module.id,
    title: lesson.title,
    durationMinutes: lesson.durationMinutes,
    theory: lesson.theory,
    examples: [
      `Слабый промпт: ${lesson.badPrompt}`,
      `Улучшенный промпт: ${lesson.goodPrompt}`,
      lesson.exampleExplanation
    ],
    practiceTask: lesson.practiceTask,
    keyTakeaway: lesson.keyTakeaway,
    quiz: createQuiz(lesson)
  }))
}));

export const allLessons = courseModules.flatMap((module) => module.lessons);

export const lessonIds = allLessons.map((lesson) => lesson.id);

export const totalDurationMinutes = allLessons.reduce(
  (sum, lesson) => sum + lesson.durationMinutes,
  0
);

export const getLessonById = (lessonId: string) =>
  allLessons.find((lesson) => lesson.id === lessonId);

export const getModuleByLessonId = (lessonId: string) =>
  courseModules.find((module) => module.lessons.some((lesson) => lesson.id === lessonId));
